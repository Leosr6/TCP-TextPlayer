    //Sets the text on jTextArea1
    private void displayText(List<String> text)
    {
        if (!text.isEmpty())
        {
            int i;
            jTextArea1.setText(null); //clear the existing text
            for (i = 0; i < text.size()-1; i++)
            {
                jTextArea1.append(text.get(i) + "\n");
            }
            jTextArea1.append(text.get(i));
        }
    }
